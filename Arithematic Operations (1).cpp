#include<stdio.h>
int main()
{
	int a,b ;
	printf("Enter two numbers :");
	scanf("%d%d",&a,&b);
	printf("Addition of the two numbers is :%d\n",a+b);
	printf("Subtraction of the two numbers is :%d\n",a-b);
	printf("Multiplication of the two numbers is :%d\n",a*b);
	printf("Division of the two numbers is :%d\n",a/b);
	printf("Remainder after divison of the two numbers is :%d\n",a%b);
	return 0;
}
